// abaixo, cada linha há uma expressão que combina
// comparadores e operadores lógicos
// sua tarefa consiste em escrever o que deve ser o
// resultado de cada um ao lado, como no exemplo

// exemplo:
console.log(2 < 4); // resultado: true

// exercícios:

console.log(2.5 < 4 && 2.5 < 3); // resultado: true

console.log(3.14 >= 4 && 3.14 >= 0); // resultado: false

console.log(-1 < -4 || -1 > -4); // resultado: true

// você pode usar parêntesis quando necessário
console.log(!(10 < 3)); // resultado: true

console.log(10 >= 3); // resultado: true
